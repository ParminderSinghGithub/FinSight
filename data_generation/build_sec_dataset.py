def _project_root() -> Path:
    return Path(__file__).resolve().parent.parent


def _data_main_root() -> Path:
    # Local path for main dataset build in this repository.
    return _project_root() / "data_main"


def _text_root() -> Path:
    return _data_main_root() / "raw" / "text"


def _sec_dir() -> Path:
    return _text_root() / "sec_filings"


def _processed_dir() -> Path:
    return _data_main_root() / "processed"


def _text_metadata_path() -> Path:
    return _processed_dir() / "text_metadata.json"


def _logs_dir() -> Path:
    return _processed_dir() / LOG_DIR_NAME


def ensure_text_dirs() -> None:
    _sec_dir().mkdir(parents=True, exist_ok=True)
    _processed_dir().mkdir(parents=True, exist_ok=True)


def _configure_logging() -> Path:
    ensure_text_dirs()
    _logs_dir().mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    log_path = _logs_dir() / f"build_text_dataset_{timestamp}.log"

    logger.handlers.clear()
    logger.setLevel(logging.INFO)
    logger.propagate = False

    formatter = logging.Formatter("%(asctime)s | %(levelname)s | %(message)s")

    stream_handler = logging.StreamHandler()
    stream_handler.setFormatter(formatter)
    logger.addHandler(stream_handler)

    file_handler = logging.FileHandler(log_path, encoding="utf-8")
    file_handler.setFormatter(formatter)
    logger.addHandler(file_handler)

    return log_path


def _reset_text_outputs() -> dict[str, int]:
    ensure_text_dirs()

    removed_sec = 0

    for file_path in _sec_dir().glob("*.txt"):
        file_path.unlink(missing_ok=True)
        removed_sec += 1

    metadata_removed = 0
    metadata_path = _text_metadata_path()
    if metadata_path.exists():
        metadata_path.unlink(missing_ok=True)
        metadata_removed = 1

    return {
        "removed_sec": removed_sec,
        "metadata_removed": metadata_removed,
    }


def _sec_headers() -> dict[str, str]:
    user_agent = os.getenv(
        "SEC_USER_AGENT",
        "FintechRAGDatasetBuilder/1.0 (contact: support@p2.local)",
    )
    return {
        "User-Agent": user_agent,
        "Accept": "application/json, text/html;q=0.9, */*;q=0.8",
        "Accept-Encoding": "gzip, deflate",
        "Connection": "keep-alive",
    }


def _download_json(url: str, headers: dict[str, str]) -> dict:
    last_exc: Exception | None = None
    for attempt in range(REQUEST_RETRIES):
        try:
            resp = requests.get(url, headers=headers, timeout=(8, REQUEST_TIMEOUT))
            resp.raise_for_status()
            return resp.json()
        except requests.RequestException as exc:
            last_exc = exc
            logger.warning(
                "JSON request failed (attempt %s/%s): %s | url=%s",
                attempt + 1,
                REQUEST_RETRIES,
                exc,
                url,
            )
            if attempt < REQUEST_RETRIES - 1:
                time.sleep(REQUEST_SLEEP_SECONDS * (attempt + 1))
    if last_exc is not None:
        raise last_exc
    raise RuntimeError("SEC JSON download failed without exception context")


def _download_text(url: str, headers: dict[str, str]) -> str:
    last_exc: Exception | None = None
    for attempt in range(REQUEST_RETRIES):
        try:
            resp = requests.get(url, headers=headers, timeout=(8, REQUEST_TIMEOUT))
            resp.raise_for_status()
            return resp.text
        except requests.RequestException as exc:
            last_exc = exc
            logger.warning(
                "Text request failed (attempt %s/%s): %s | url=%s",
                attempt + 1,
                REQUEST_RETRIES,
                exc,
                url,
            )
            if attempt < REQUEST_RETRIES - 1:
                time.sleep(REQUEST_SLEEP_SECONDS * (attempt + 1))
    if last_exc is not None:
        raise last_exc
    raise RuntimeError("SEC text download failed without exception context")


def _strip_tables(html_text: str) -> str:
    without_tables = re.sub(
        r"<table[\s\S]*?</table>",
        " ",
        html_text,
        flags=re.IGNORECASE,
    )
    return without_tables


def _clean_html_to_text(raw_html: str) -> str:
    text = _strip_tables(raw_html)
    text = re.sub(r"<script[\s\S]*?</script>", " ", text, flags=re.IGNORECASE)
    text = re.sub(r"<style[\s\S]*?</style>", " ", text, flags=re.IGNORECASE)
    text = re.sub(r"<[^>]+>", " ", text)
    text = html.unescape(text)
    text = text.replace("\xa0", " ")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def _word_count(text: str) -> int:
    return len(re.findall(r"\b\w+\b", text))


def _normalize_for_dedupe(text: str) -> str:
    lowered = text.lower()
    lowered = re.sub(r"\s+", " ", lowered)
    return lowered.strip()


def _extract_sections_from_10k(clean_text: str) -> str:
    compact = re.sub(r"\s+", " ", clean_text)

    lower = compact.lower()

    def find_first(markers: list[str], start: int = 0) -> int:
        positions = [lower.find(marker, start) for marker in markers]
        positions = [p for p in positions if p != -1]
        return min(positions) if positions else -1

    def extract_between(start_markers: list[str], end_markers: list[str]) -> str:
        start_idx = find_first(start_markers)
        if start_idx == -1:
            return ""
        # Jump past marker label to the body content.
        marker_len = max(len(m) for m in start_markers)
        body_start = start_idx + marker_len

        end_idx = find_first(end_markers, body_start)
        if end_idx == -1:
            end_idx = len(compact)

        section = compact[body_start:end_idx]
        section = re.sub(r"item\s*\d+[a-z]?\.?", " ", section, flags=re.IGNORECASE)
        section = re.sub(r"\s+", " ", section).strip()
        return section

    business = extract_between(
        ["item 1 business", "item 1. business"],
        ["item 1a", "item 1b", "item 2"],
    )
    risk = extract_between(
        ["item 1a risk factors", "item 1a. risk factors"],
        ["item 1b", "item 2", "item 3"],
    )
    mda = extract_between(
        [
            "item 7 management's discussion and analysis",
            "item 7. management's discussion and analysis",
            "item 7 managements discussion and analysis",
            "item 7. managements discussion and analysis",
        ],
        ["item 7a", "item 8", "item 9"],
    )

    sections: list[str] = []
    for sec in (business, risk, mda):
        if _word_count(sec) >= 150:
            sections.append(sec)

    if not sections:
        return ""

    merged = "\n\n".join(sections)
    merged = re.sub(r"\s+", " ", merged).strip()
    return merged


def _fallback_extract_from_10k(clean_text: str, min_words: int = 500, max_words: int = 3500) -> str:
    compact = re.sub(r"\s+", " ", clean_text).strip()
    if not compact:
        return ""

    lower = compact.lower()

    start_markers = ["item 1 business", "item 1. business", "item 1 "]
    start_idx = -1
    for marker in start_markers:
        pos = lower.find(marker)
        if pos != -1:
            start_idx = pos
            break
    if start_idx == -1:
        start_idx = 0

    end_markers = ["item 8 ", "item 9 ", "signatures"]
    end_idx = -1
    for marker in end_markers:
        pos = lower.find(marker, start_idx + 1)
        if pos != -1:
            end_idx = pos
            break
    if end_idx == -1:
        end_idx = len(compact)

    span = compact[start_idx:end_idx].strip()
    if not span:
        all_words = compact.split()
        if len(all_words) < min_words:
            return ""
        return " ".join(all_words[:max_words])

    words = span.split()
    if len(words) > max_words:
        words = words[:max_words]
    if len(words) < min_words:
        all_words = compact.split()
        if len(all_words) < min_words:
            return ""
        return " ".join(all_words[:max_words])
    return " ".join(words)


def _iter_company_filings(cik: str) -> Iterator[tuple[str, str]]:
    headers = _sec_headers()
    submissions = _download_json(SEC_SUBMISSIONS_URL.format(cik=cik), headers=headers)

    seen_accessions: set[str] = set()

    def yield_from_block(block: dict) -> Iterator[tuple[str, str]]:
        forms = block.get("form", [])
        accessions = block.get("accessionNumber", [])
        primary_docs = block.get("primaryDocument", [])

        for form, accession, primary_doc in zip(forms, accessions, primary_docs):
            if form not in SEC_FORM_TYPES:
                continue
            if not isinstance(accession, str) or not accession.strip():
                continue
            if not isinstance(primary_doc, str) or not primary_doc.strip():
                logger.warning(
                    "Skipping SEC filing with missing primary document | cik=%s accession=%s form=%s",
                    cik,
                    accession,
                    form,
                )
                continue
            if accession in seen_accessions:
                continue
            seen_accessions.add(accession)
            accession_no_dash = accession.replace("-", "")
            filing_url = SEC_FILINGS_URL.format(cik=int(cik), accession=accession_no_dash, primary=primary_doc)
            yield accession, filing_url

    recent = submissions.get("filings", {}).get("recent", {})
    for row in yield_from_block(recent):
        yield row

    history_files = submissions.get("filings", {}).get("files", [])
    for history_file in history_files:
        name = history_file.get("name")
        if not name:
            continue
        history_url = f"https://data.sec.gov/submissions/{name}"
        history_json = _download_json(history_url, headers=headers)
        for row in yield_from_block(history_json):
            yield row


def _next_id_from_dir(dir_path: Path, prefix: str) -> int:
    max_id = 0
    for path in dir_path.glob(f"{prefix}_*.txt"):
        stem = path.stem
        try:
            value = int(stem.split("_")[1])
            max_id = max(max_id, value)
        except (IndexError, ValueError):
            continue
    return max_id + 1


def _save_text_file(path: Path, text: str) -> None:
    path.write_text(text.strip() + "\n", encoding="utf-8")


def _append_metadata(entries: list[dict]) -> None:
    metadata_path = _text_metadata_path()
    existing: list[dict] = []
    if metadata_path.exists():
        try:
            existing = json.loads(metadata_path.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            existing = []

    existing.extend(entries)
    metadata_path.write_text(json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8")


def _load_existing_hashes(folder: Path) -> set[str]:
    hashes: set[str] = set()
    for file_path in folder.glob("*.txt"):
        try:
            text = file_path.read_text(encoding="utf-8", errors="ignore")
        except OSError:
            continue
        normalized = _normalize_for_dedupe(text)
        if not normalized:
            continue
        digest = hashlib.sha1(normalized.encode("utf-8")).hexdigest()
        hashes.add(digest)
    return hashes


def _is_valid_utf8(text: str) -> bool:
    try:
        text.encode("utf-8")
        return True
    except UnicodeEncodeError:
        return False


def _passes_quality_filters(text: str, min_words: int, max_words: int | None = None) -> bool:
    if not text or not text.strip():
        return False
    if not _is_valid_utf8(text):
        return False
    wc = _word_count(text)
    if wc <= min_words:
        return False
    if max_words is not None and wc > max_words:
        return False
    return True


def build_sec_dataset(target_min: int = TARGET_SEC_MIN, target_max: int = TARGET_SEC_MAX) -> TextBuildStats:
    logger.info("Downloading SEC filings")
    logger.info("SEC target range: min=%s max=%s", target_min, target_max)

    stats = TextBuildStats()
    sec_path = _sec_dir()
    next_id = _next_id_from_dir(sec_path, "sec")

    seen_hashes: set[str] = _load_existing_hashes(sec_path)
    logger.info("Loaded %s existing SEC content hashes for incremental dedupe", len(seen_hashes))
    metadata_batch: list[dict] = []

    for ticker, cik in COMPANY_CIKS.items():
        logger.info("Starting ticker %s (CIK %s)", ticker, cik)
        ticker_saved_before = stats.sec_saved
        ticker_short_before = stats.sec_skipped_short
        ticker_processed = 0
        try:
            for accession, filing_url in _iter_company_filings(cik):
                if stats.sec_saved >= target_max:
                    break
                if ticker_processed >= MAX_FILINGS_PER_COMPANY:
                    logger.info(
                        "Reached per-company filing cap (%s) for %s",
                        MAX_FILINGS_PER_COMPANY,
                        ticker,
                    )
                    break

                try:
                    raw_html = _download_text(filing_url, headers=_sec_headers())
                    clean_text = _clean_html_to_text(raw_html)
                    selected = _extract_sections_from_10k(clean_text)
                    ticker_processed += 1

                    if not _passes_quality_filters(selected, SEC_MIN_WORDS):
                        selected = _fallback_extract_from_10k(
                            clean_text,
                            min_words=SEC_FALLBACK_MIN_WORDS,
                            max_words=SEC_FALLBACK_MAX_WORDS,
                        )

                    if not _passes_quality_filters(selected, SEC_MIN_WORDS):
                        stats.sec_skipped_short += 1
                        if stats.sec_skipped_short <= 10 or stats.sec_skipped_short % 50 == 0:
                            logger.warning(
                                "SEC extraction too short after fallback | ticker=%s accession=%s url=%s clean_words=%s selected_words=%s",
                                ticker,
                                accession,
                                filing_url,
                                _word_count(clean_text),
                                _word_count(selected),
                            )
                        time.sleep(REQUEST_SLEEP_SECONDS)
                        continue

                    digest = hashlib.sha1(_normalize_for_dedupe(clean_text).encode("utf-8")).hexdigest()
                    if digest in seen_hashes:
                        stats.sec_skipped_duplicate += 1
                        time.sleep(REQUEST_SLEEP_SECONDS)
                        continue

                    seen_hashes.add(digest)
                    filename = f"sec_{next_id:05d}.txt"
                    file_path = sec_path / filename
                    _save_text_file(file_path, selected)

                    metadata_batch.append(
                        {
                            "doc_id": f"sec_{next_id:05d}",
                            "modality": "text",
                            "source_file": str(file_path.as_posix()),
                            "language": "en",
                            "section": "business+risk+mda",
                            "char_count": len(selected),
                            "extra": {
                                "source": "sec-edgar",
                                "ticker": ticker,
                                "cik": cik,
                                "accession": accession,
                                "form": "10-K/10-Q",
                                "url": filing_url,
                            },
                        }
                    )
                    stats.sec_saved += 1
                    next_id += 1

                    if len(metadata_batch) >= BATCH_SIZE_SEC:
                        _append_metadata(metadata_batch)
                        metadata_batch = []
                        logger.info("Flushed SEC metadata batch (%s entries)", BATCH_SIZE_SEC)

                    if stats.sec_saved % 100 == 0:
                        logger.info("Saved %s SEC documents", stats.sec_saved)

                    time.sleep(REQUEST_SLEEP_SECONDS)
                except requests.RequestException as exc:
                    logger.warning(
                        "SEC filing download failed | ticker=%s accession=%s url=%s error=%s",
                        ticker,
                        accession,
                        filing_url,
                        exc,
                    )
                    time.sleep(REQUEST_SLEEP_SECONDS)
                    continue

        except requests.RequestException as exc:
            logger.warning("SEC submissions fetch failed | ticker=%s cik=%s error=%s", ticker, cik, exc)
            continue
        finally:
            saved_for_ticker = stats.sec_saved - ticker_saved_before
            short_for_ticker = stats.sec_skipped_short - ticker_short_before
            logger.info("Finished ticker %s | saved=%s short_skips=%s", ticker, saved_for_ticker, short_for_ticker)

    if metadata_batch:
        _append_metadata(metadata_batch)
        logger.info("Flushed final SEC metadata batch (%s entries)", len(metadata_batch))

    if stats.sec_saved < target_min:
        logger.warning(
            "Saved %s SEC documents, below target minimum %s. "
            "Network, SEC throttling, or extraction constraints may have limited output."
            ,
            stats.sec_saved,
            target_min,
        )

    logger.info(
        "SEC summary | saved=%s skipped_short=%s skipped_duplicate=%s",
        stats.sec_saved,
        stats.sec_skipped_short,
        stats.sec_skipped_duplicate,
    )
    return stats


def validate_text_dataset() -> tuple[int, int]:
    removed_empty = 0
    removed_duplicates = 0

    seen: set[str] = set()
    for file_path in sorted(_sec_dir().glob("*.txt")):
        text = file_path.read_text(encoding="utf-8", errors="ignore").strip()
        if not text:
            file_path.unlink(missing_ok=True)
            removed_empty += 1
            continue

        digest = hashlib.sha1(_normalize_for_dedupe(text).encode("utf-8")).hexdigest()
        if digest in seen:
            file_path.unlink(missing_ok=True)
            removed_duplicates += 1
            continue
        seen.add(digest)

    return removed_empty, removed_duplicates


def build_text_dataset(clean_start: bool = False) -> dict[str, int]:
    log_path = _configure_logging()
    logger.info("Text dataset builder started")
    logger.info("Real-data-only mode enabled; synthetic generation is disabled in this module")
    logger.info("Build log file: %s", log_path.as_posix())

    if clean_start:
        cleanup_stats = _reset_text_outputs()
        logger.info(
            "Pre-build cleanup complete | removed_sec=%s metadata_removed=%s",
            cleanup_stats["removed_sec"],
            cleanup_stats["metadata_removed"],
        )
    else:
        logger.info("Pre-build cleanup skipped (clean_start=False)")

    sec_stats = build_sec_dataset()

    removed_empty, removed_duplicates = validate_text_dataset()

    logger.info(
        "Validation summary | removed_empty=%s removed_duplicates=%s",
        removed_empty,
        removed_duplicates,
    )
    total_docs = sec_stats.sec_saved
    logger.info("TEXT DATASET SUMMARY")
    logger.info("SEC count: %s", sec_stats.sec_saved)
    logger.info("Total documents: %s", total_docs)
    logger.info("Text dataset build complete")

    return {
        "sec_saved": sec_stats.sec_saved,
        "news_saved": 0,
        "removed_empty": removed_empty,
        "removed_duplicates": removed_duplicates,
        "total_docs": total_docs,
    }


def main() -> None:
    build_text_dataset()


if __name__ == "__main__":
    main()
